# Which of the following are objects in Ruby? If they are objects, how can you find out what class they belong to?
#
# true
# "hello"
# [1, 2, 3, "happy days"]
# 142

# true => object, Class TrueClass
# "hello" => object, Class String
# [1, 2, 3, "happy days"] => object, Class Array
# 142 => object, Class Integer

p true.class
p "hello".class
p [1, 2, 3, "happy days"].class
p 142.class
